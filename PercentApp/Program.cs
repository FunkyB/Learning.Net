using System.Globalization;

Calculate(Console.ReadLine());

static double Calculate(string input)
{
    var newList = input.Split(' ').ToList();

    double accountSum = Convert.ToDouble(newList[0], );
    double accoutPercent = Convert.ToDouble(newList[1]);
    int accountPeriod = Convert.ToInt32(newList[2]);

    return accountSum + (accoutPercent / 12) * accountPeriod;
}